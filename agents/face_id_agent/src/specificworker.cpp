void SpecificWorker::initialize(int period)
{
	std::cout << "Initialize worker" << std::endl;
	this->Period = period;
	if(this->startup_check_flag)
	{
		this->startup_check();
	}
	else
	{
		timer.start(Period);
		// create graph
		G = std::make_shared<DSR::DSRGraph>(0, agent_name, agent_id, ""); // Init nodes
		std::cout<< __FUNCTION__ << "Graph loaded" << std::endl;  

		//dsr update signals
//		connect(G.get(), &DSR::DSRGraph::update_node_signal, this, &SpecificWorker::modify_node_slot);
//		connect(G.get(), &DSR::DSRGraph::update_edge_signal, this, &SpecificWorker::modify_edge_slot);
//		connect(G.get(), &DSR::DSRGraph::update_node_attr_signal, this, &SpecificWorker::modify_attrs_slot);
//		connect(G.get(), &DSR::DSRGraph::del_edge_signal, this, &SpecificWorker::del_edge_slot);
//		connect(G.get(), &DSR::DSRGraph::del_node_signal, this, &SpecificWorker::del_node_slot);

		// Graph viewer
		using opts = DSR::DSRViewer::view;
		int current_opts = 0;
		opts main = opts::none;
		if(tree_view)
		{
		    current_opts = current_opts | opts::tree;
		}
		if(graph_view)
		{
		    current_opts = current_opts | opts::graph;
		    main = opts::graph;
		}
		if(qscene_2d_view)
		{
		    current_opts = current_opts | opts::scene;
		}
		if(osg_3d_view)
		{
		    current_opts = current_opts | opts::osg;
		}
		graph_viewer = std::make_unique<DSR::DSRViewer>(this, G, current_opts, main);
		setWindowTitle(QString::fromStdString(agent_name + "-") + QString::number(agent_id));

		this->Period = period;
		timer.start(Period);
        hide();
	}

}

void SpecificWorker::compute()
{
    if(auto world_node = G->get_node("world"); world_node.has_value()) {
        bool existing_name = false;
        auto person_nodes = G->get_nodes_by_type("person");
        if (person_nodes.size() == 0)
        {
            qInfo() << "NO PEOPLE";
            return;
        }
        else
        {
            if(auto person_node_name = G->get_attrib_by_name<person_name_att>(person_nodes[0]); !person_node_name.has_value())
            {
                auto recognised_person = this->realsensefaceid_proxy->authenticate();
                if (recognised_person.size() > 0)
                {
                    auto person_name_str = recognised_person[0].userAuthenticated;
                    cout << "RECOGNISED PERSON: " << person_name_str << endl;
                    G->add_or_modify_attrib_local<person_name_att>(person_nodes[0], person_name_str);
                    G->update_node(person_nodes[0]);
                }
            }
        }
    }
}

